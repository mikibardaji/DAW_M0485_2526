public class Animales {
    private String tipo;
    private String ruido;

    public Animales(String tipo, String ruido) {
        this.tipo = tipo;
        this.ruido = ruido;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getRuido() {
        return ruido;
    }

    public void setRuido(String ruido) {
        this.ruido = ruido;
    }
    
    
    
}
